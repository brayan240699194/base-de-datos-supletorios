package Entidad;

public class ClsEntidadUsuario {

    private String strIdUsuario;
    private String strNombreUsuario;
    private String strApellidoUsuario;
    private String strCorreoUsuario;
    private String strPasswordUsuario;

    public String getStrIdUsuario() {
        return strIdUsuario;
    }

    /**
     * @param strIdUsuario the strIdUsuario to set
     */
    public void setStrIdUsuario(String strIdUsuario) {
        this.strIdUsuario = strIdUsuario;
    }

    /**
     * @return the strNombreUsuario
     */
    public String getStrNombreUsuario() {
        return strNombreUsuario;
    }

    /**
     * @param strNombreUsuario the strNombreUsuario to set
     */
    public void setStrNombreUsuario(String strNombreUsuario) {
        this.strNombreUsuario = strNombreUsuario;
    }

    /**
     * @return the strApellidoUsuario
     */
    public String getStrApellidoUsuario() {
        return strApellidoUsuario;
    }

    /**
     * @param strApellidoUsuario the strApellidoUsuario to set
     */
    public void setStrApellidoUsuario(String strApellidoUsuario) {
        this.strApellidoUsuario = strApellidoUsuario;
    }

    /**
     * @return the strCorreoUsuario
     */
    public String getStrCorreoUsuario() {
        return strCorreoUsuario;
    }

    /**
     * @param strCorreoUsuario the strCorreoUsuario to set
     */
    public void setStrCorreoUsuario(String strCorreoUsuario) {
        this.strCorreoUsuario = strCorreoUsuario;
    }

    /**
     * @return the strPasswordUsuario
     */
    public String getStrPasswordUsuario() {
        return strPasswordUsuario;
    }

    /**
     * @param strPasswordUsuario the strPasswordUsuario to set
     */
    public void setStrPasswordUsuario(String strPasswordUsuario) {
        this.strPasswordUsuario = strPasswordUsuario;
    }

}
